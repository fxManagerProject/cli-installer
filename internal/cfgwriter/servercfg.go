const licenseLine = `sv_licenseKey "%s"`
const licenseLinePlaceholder = `# sv_licenseKey "REPLACE_WITH_YOUR_CFX_LICENSE_KEY"  # get one at https://portal.cfx.re/servers/registration-keys?modal=create`

var licenseLineRe = regexp.MustCompile(`(?m)^\s*#?\s*sv_licenseKey\s+".*"\s*$`)

// Write creates server.cfg at path if it doesn't exist yet, using the
// default template. If it already exists (e.g. re-running the
// installer against an existing server-data/), Write only touches the
// sv_licenseKey line via Inject, leaving everything else alone
func Write(path string, opts Options) error {
	if _, err := os.Stat(path); err == nil {
		return Inject(path, opts)
	}

	license := licenseLinePlaceholder
	if opts.License != "" {
		license = fmt.Sprintf(licenseLine, opts.License)
	}

	content := fmt.Sprintf(defaultTemplate, license)
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		return fmt.Errorf("writing %s: %w", path, err)
	}
	return nil
}

// Inject replaces (or appends) the sv_licenseKey line in an existing
// server.cfg without touching anything else in the file. Safe to call
// even if opts.License is empty (it's then a no-op)
func Inject(path string, opts Options) error {
	if opts.License == "" {
		return nil
	}

	raw, err := os.ReadFile(path)
	if err != nil {
		return fmt.Errorf("reading %s: %w", path, err)
	}

	newLine := fmt.Sprintf(licenseLine, opts.License)

	var out string
	if licenseLineRe.MatchString(string(raw)) {
		out = licenseLineRe.ReplaceAllString(string(raw), newLine)
	} else {
		out = strings.TrimRight(string(raw), "\n") + "\n\n" + newLine + "\n"
	}

	if err := os.WriteFile(path, []byte(out), 0o644); err != nil {
		return fmt.Errorf("writing %s: %w", path, err)
	}
	return nil
}
